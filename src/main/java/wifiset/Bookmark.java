package wifiset;


public class Bookmark {
	private int bookId;
	private String bookName;
	private int bookOder;
	private String bookRegDate;
	private String bookModiDate;
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public int getBookOder() {
		return bookOder;
	}
	public void setBookOder(int bookOder) {
		this.bookOder = bookOder;
	}
	public String getBookRegDate() {
		return bookRegDate;
	}
	public void setBookRegDate(String bookRegDate) {
		this.bookRegDate = bookRegDate;
	}
	public String getBookModiDate() {
		return bookModiDate;
	}
	public void setBookModiDate(String bookModiDate) {
		this.bookModiDate = bookModiDate;
	}
	
	
	

}
