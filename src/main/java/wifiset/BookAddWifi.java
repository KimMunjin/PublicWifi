package wifiset;

public class BookAddWifi {
	private int bookAddNum;
	private int bookId;
	private String mgrNo;
	private String bkwiRegDate;
	private String bookName;
	private String wifiName;
	
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getWifiName() {
		return wifiName;
	}
	public void setWifiName(String wifiName) {
		this.wifiName = wifiName;
	}
	public int getBookAddNum() {
		return bookAddNum;
	}
	public void setBookAddNum(int bookAddNum) {
		this.bookAddNum = bookAddNum;
	}
	public int getBookId() {
		return bookId;
	}
	public void setBookId(int bookId) {
		this.bookId = bookId;
	}
	public String getMgrNo() {
		return mgrNo;
	}
	public void setMgrNo(String mgrNo) {
		this.mgrNo = mgrNo;
	}
	public String getBkwiRegDate() {
		return bkwiRegDate;
	}
	public void setBkwiRegDate(String bkwiRegDate) {
		this.bkwiRegDate = bkwiRegDate;
	}
	
	
}
